/**
 * 단일 카드 결과에 연애 상태 수정자를 적용합니다.
 * - result.emotionFlow 끝에 lens 문장 추가
 * - result.advice 마지막 항목을 상태별 조언으로 대체
 *
 * @param {string} cardId
 * @param {Object} result - { summary, emotionTags, emotionFlow, advice, caution }
 * @param {string|null} status - 'single' | 'situationship' | 'dating' | 'breakup' | null
 * @returns {Object} 수정된 결과
 */
export function applyRelationshipModifier(cardId, result, status) {
  if (!status || !result) return result
  const mods = RELATIONSHIP_MODIFIERS[cardId]
  if (!mods) return result
  const mod = mods[status]
  if (!mod) return result

  return {
    ...result,
    emotionFlow: [...result.emotionFlow, mod.lens],
    advice: result.advice.length > 0
      ? [...result.advice.slice(0, -1), mod.advice]
      : [mod.advice],
  }
}

/**
 * 멀티 카드 전체 요약(overall)에 연애 상태 렌즈를 적용합니다.
 * overall.guidance 배열 끝에 상태별 문장 추가.
 *
 * @param {Object} overall - { currentEmotion, guidance/hiddenFeeling, summary, ... }
 * @param {string|null} status
 * @returns {Object} 수정된 overall
 */
export function applyRelationshipModifierToOverall(overall, status) {
  if (!status || !overall) return overall
  const lens = OVERALL_RELATIONSHIP_LENS[status]
  if (!lens) return overall

  // guidance 키가 있으면 그 배열에 추가, 없으면 advice에 추가
  if (Array.isArray(overall.guidance)) {
    return { ...overall, guidance: [...overall.guidance, lens] }
  }
  if (Array.isArray(overall.advice)) {
    return { ...overall, advice: [...overall.advice, lens] }
  }
  return overall
}

